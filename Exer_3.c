#include<stdio.h>
#include<stdlib.h>

int main (){
	
	double r, b, cal, area, perimetro;
	const float Pi = 3.1416;
	printf("Digite numericamente o raio da circunferencia: ");
	scanf("%d", &r);
	cal = r * Pi;
	b == r;
	area = b * cal;
	perimetro = 2 * Pi * r;
	printf("\nA area do circulo e %d", area);
	printf("\nO perimetro do circulo e %d", perimetro);
		
}